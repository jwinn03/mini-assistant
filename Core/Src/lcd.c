static void HAL_LTDC_LineEventCallback_impl(LTDC_HandleTypeDef *hltdc_ptr);

void HAL_LTDC_LineEventCallback(LTDC_HandleTypeDef *hltdc_ptr)
{
    HAL_LTDC_LineEventCallback_impl(hltdc_ptr);
}

static void HAL_LTDC_LineEventCallback_impl(LTDC_HandleTypeDef *hltdc_ptr)
{
    BaseType_t higher_priority_task_woken = pdFALSE;
    if (vblank_sem != NULL) {
        xSemaphoreGiveFromISR(vblank_sem, &higher_priority_task_woken);
    }
    /* Re-arm for the next frame. */
    HAL_LTDC_ProgramLineEvent(hltdc_ptr, LCD_H);
    portYIELD_FROM_ISR(higher_priority_task_woken);
}

void lcd_init(void)
{
    vblank_sem = xSemaphoreCreateBinary();
    lcd_clear(LCD_BLACK);
    /* Arm the line-event interrupt to fire at the last active scan line.
       Once armed, HAL_LTDC_LineEventCallback re-arms itself each frame. */
    HAL_LTDC_ProgramLineEvent(&hltdc, LCD_H);
}

void lcd_clear(uint16_t color)
{
    /* memset only works for 0x00 or 0xFF colors; otherwise loop. */
    if (color == 0x0000 || color == 0xFFFF) {
        memset(LCD_FB, (color == 0xFFFF) ? 0xFF : 0x00, LCD_W * LCD_H * 2);
        return;
    }
    uint32_t n = LCD_W * LCD_H;
    uint16_t *p = LCD_FB;
    while (n--) *p++ = color;
}

void lcd_fill_rect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    if (x >= LCD_W || y >= LCD_H) return;
    if (x + w > LCD_W) w = LCD_W - x;
    if (y + h > LCD_H) h = LCD_H - y;
    for (uint16_t row = 0; row < h; row++) {
        uint16_t *p = &LCD_FB[(y + row) * LCD_W + x];
        for (uint16_t col = 0; col < w; col++) *p++ = color;
    }
}

